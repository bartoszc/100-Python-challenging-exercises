def square():
    """Question:
    Define a function which can print a dictionary where the keys are numbers between 1 and 3 (both included) and the
    values are square of keys.
    """
    new_d = dict()
    new_d[1] = 1**1
    new_d[2] = 2**2
    new_d[3] = 3**2
    print(new_d)


square()
